"""
We can use the type function to find out what data type a variable is bound
to.
"""


this_variable = 1
that = 1.0
something_else = "1.0"
another_thing = False
nothing = None

print(this_variable)
print(that)
print(something_else)
print(another_thing)
print(nothing)

print(type(this_variable))
print(type(that))
print(type(something_else))
print(type(another_thing))
print(type(nothing))
