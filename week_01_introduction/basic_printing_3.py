"""
We can print anything: strings, floating point numbers, integers, booleans...
"""

# Printing a string (a sequence of Unicode characters)
print('Captain Janeway > Captain Kirk. Change my mind.')

# Printing a floating point number (a number with a decimal)
print(3.13)

# Printing an integer
print(1)

# Printing a boolean
print(True)

# Printing "nothing"
print(None)
